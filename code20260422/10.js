/**
 * 题目描述
一个有N个选手参加比赛，选手编号为1~N（3<=N<=100），有M（3<=M<=10）个评委对选手进行打分。
打分规则为每个评委对选手打分，最高分10分，最低分1分。
请计算得分最多的3位选手的编号。
如果得分相同，则得分高分值最多的选手排名靠前
(10分数量相同，则比较9分的数量，以此类推，用例中不会出现多个选手得分完全相同的情况)。
输入描述
第一行为半角逗号分割的两个正整数，第一个数字表示M（3<=M<=10）个评委，第二个数字表示N（3<=N<=100）个选手。
第2到M+1行是半角逗号分割的整数序列，表示评委为每个选手的打分，0号下标数字表示1号选手分数，1号下标数字表示2号选手分数，依次类推。
输出描述
选手前3名的编号。
注：若输入为异常，输出-1，如M、N、打分不在范围内。
用例
输入	4,5
10,6,9,7,6
9,10,6,7,5
8,10,6,5,10
9,10,8,4,9
输出	2,1,5
说明	
第一行代表有4个评委，5个选手参加比赛

矩阵代表是4*5，每个数字是选手的编号，每一行代表一个评委对选手的打分排序，

2号选手得分36分排第1，1号选手36分排第2，5号选手30分(2号10分值有3个，1号10分值只有1个，所以2号排第一)

输入	2,5
7,3,5,4,2
8,5,4,4,3
输出	-1
说明	只有2个评委，要求最少为3个评委
输入	4,2
8,5
5,6
10,4
8,9
输出	-1
说明	只有2名选手参加，要求最少为3名
输入	4,5
11,6,9,7,8
9,10,6,7,8
8,10,6,9,7
9,10,8,6,7
输出	-1
说明	第一个评委给第一个选手打分11，无效分数
题目解析
考察数组排序，主要难点在于下面逻辑的设计

如果得分相同，则得分高分值最多的选手排名靠前

我这里直接将选手分数降序排序后，join('')为字符串数值，高分多的选手该字符串数值越大
 */

// 定义主函数，lines 表示按题目输入顺序组织好的所有文本行。
const getTop3Players = (lines) => {
  // 如果输入不是数组，直接返回异常结果。
  if (!Array.isArray(lines) || lines.length === 0) {
    // 返回 -1 表示输入异常。
    return '-1'
  }

  // 取出第一行的评委数和选手数描述。
  const firstLine = lines[0]
  // 按逗号拆分第一行。
  const firstParts = firstLine.split(',')

  // 如果第一行拆分后不是两个数字，则输入异常。
  if (firstParts.length !== 2) {
    // 返回 -1。
    return '-1'
  }

  // 解析评委数量 M。
  const m = Number(firstParts[0])
  // 解析选手数量 N。
  const n = Number(firstParts[1])

  // 如果 M、N 不是整数，或者不在题目要求范围内，则返回异常结果。
  if (!Number.isInteger(m) || !Number.isInteger(n) || m < 3 || m > 10 || n < 3 || n > 100) {
    // 返回 -1。
    return '-1'
  }

  // 如果后续评分行数和 M 不一致，则输入异常。
  if (lines.length !== m + 1) {
    // 返回 -1。
    return '-1'
  }

  // 创建选手统计数组，下标 0 对应 1 号选手。
  const players = new Array(n).fill(0).map((_, index) => ({
    // 记录选手编号。
    id: index + 1,
    // 记录选手总分。
    total: 0,
    // 记录每种分值出现的次数，索引 1 到 10 有效。
    scoreCount: new Array(11).fill(0)
  }))

  // 逐行处理每个评委给出的打分。
  for (let i = 1; i <= m; i++) {
    // 取出当前评委这一行评分文本。
    const row = lines[i]
    // 按逗号拆分成每位选手的分数。
    const scores = row.split(',')

    // 如果当前行评分数量和选手数量不一致，则输入异常。
    if (scores.length !== n) {
      // 返回 -1。
      return '-1'
    }

    // 遍历当前评委给每位选手的打分。
    for (let j = 0; j < n; j++) {
      // 把当前分数字符串转成数字。
      const score = Number(scores[j])

      // 如果当前分数不是整数，或者不在 1 到 10 之间，则输入异常。
      if (!Number.isInteger(score) || score < 1 || score > 10) {
        // 返回 -1。
        return '-1'
      }

      // 给当前选手累加总分。
      players[j].total += score
      // 统计当前分值出现的次数。
      players[j].scoreCount[score]++
    }
  }

  // 按题目要求对选手进行排序。
  players.sort((a, b) => {
    // 第一优先级：总分高的排前面。
    if (b.total !== a.total) {
      // 返回总分降序结果。
      return b.total - a.total
    }

    // 第二优先级：比较 10 分、9 分……一直到 1 分的个数。
    for (let score = 10; score >= 1; score--) {
      // 如果当前分值的数量不同。
      if (b.scoreCount[score] !== a.scoreCount[score]) {
        // 分值更高且数量更多的排前面。
        return b.scoreCount[score] - a.scoreCount[score]
      }
    }

    // 如果前面都相同，则按编号升序兜底。
    return a.id - b.id
  })

  // 取出排名前 3 的选手编号。
  const top3Ids = players
    // 只保留前 3 名。
    .slice(0, 3)
    // 映射成选手编号。
    .map((player) => player.id)

  // 按题目要求用逗号拼接返回。
  return top3Ids.join(',')
}

// 构造题目示例 1 的输入。
const lines1 = [
  // 第一行表示 4 个评委、5 个选手。
  '4,5',
  // 第 1 个评委的打分。
  '10,6,9,7,6',
  // 第 2 个评委的打分。
  '9,10,6,7,5',
  // 第 3 个评委的打分。
  '8,10,6,5,10',
  // 第 4 个评委的打分。
  '9,10,8,4,9'
]

// 打印示例 1 的结果。
console.log(getTop3Players(lines1))

