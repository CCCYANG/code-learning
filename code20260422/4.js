/**
 * 处理器问题
题目描述
某公司研发了一款高性能AI处理器。每台物理设备具备8颗AI处理器，编号分别为0、1、2、3、4、5、6、7。

编号0-3的处理器处于同一个链路中，编号4-7的处理器处于另外一个链路中，不通链路中的处理器不能通信。

如下图所示。现给定服务器可用的处理器编号数组array，以及任务申请的处理器数量num，找出符合下列亲和性调度原则的芯片组合。

如果不存在符合要求的组合，则返回空列表。

亲和性调度原则：

-如果申请处理器个数为1，则选择同一链路，剩余可用的处理器数量为1个的最佳，其次是剩余3个的为次佳，然后是剩余2个，最后是剩余4个。

-如果申请处理器个数为2，则选择同一链路剩余可用的处理器数量2个的为最佳，其次是剩余4个，最后是剩余3个。

-如果申请处理器个数为4，则必须选择同一链路剩余可用的处理器数量为4个。

-如果申请处理器个数为8，则申请节点所有8个处理器。

提示：

任务申请的处理器数量只能是1、2、4、8。
编号0-3的处理器处于一个链路，编号4-7的处理器处于另外一个链路。
处理器编号唯一，且不存在相同编号处理器。
输入描述
输入包含可用的处理器编号数组array，以及任务申请的处理器数量num两个部分。

第一行为array，第二行为num。例如：

[0, 1, 4, 5, 6, 7]
1

表示当前编号为0、1、4、5、6、7的处理器可用。任务申请1个处理器。

0 <= array.length <= 8
0 <= array[i] <= 7
num in [1, 2, 4, 8]
输出描述
输出为组合列表，当array=[0，1，4，5，6，7]，num=1 时，输出为[[0], [1]]。

用例
输入	[0, 1, 4, 5, 6, 7]
1
输出	[[0], [1]]
说明	
根据第一条亲和性调度原则，在剩余两个处理器的链路（0, 1, 2, 3）中选择处理器。

由于只有0和1可用，则返回任意一颗处理器即可。

输入	[0, 1, 4, 5, 6, 7]
4
输出	[[4, 5, 6, 7]]
说明	根据第三条亲和性调度原则，必须选择同一链路剩余可用的处理器数量为4个的环
题目解析
用例中，链路link1=[0,1]，链路link2=[4,5,6,7]

现在要选1个处理器，则需要按照亲和性调度原则

如果申请处理器个数为1，则选择同一链路，剩余可用的处理器数量为1个的最佳，其次是剩余3个的为次佳，然后是剩余2个，最后是剩余4个。

最佳的是，找剩余可用1个处理器的链路，发现没有，link1剩余可用2，link2剩余可用4

其次的是，找剩余可用3个处理器的链路，发现没有

再次的是，找剩余可用2个处理器的链路，link1符合要求，即从0和1处理器中任选一个，有两种选择，可以使用dfs找对应组合。
 */

// 定义主函数，参数 array 表示当前可用处理器，num 表示任务申请的处理器数量。
const getProcessorCombination = (array, num) => {
  // 先复制并升序排序，保证后续输出组合时顺序稳定。
  const available = [...array].sort((a, b) => a - b)
  // 过滤出第一条链路上的可用处理器，范围是 0 到 3。
  const link1 = available.filter((item) => item >= 0 && item <= 3)
  // 过滤出第二条链路上的可用处理器，范围是 4 到 7。
  const link2 = available.filter((item) => item >= 4 && item <= 7)

  // 定义一个生成组合的辅助函数，用来从某条链路中选出 size 个处理器。
  const buildCombinations = (processors, size) => {
    // result 用来保存所有合法组合。
    const result = []
    // path 用来保存当前 DFS 正在构造的组合路径。
    const path = []

    // 定义深度优先搜索函数，start 表示本轮组合从哪个下标开始选。
    const dfs = (start) => {
      // 如果当前路径长度已经等于目标 size，说明找到一个完整组合。
      if (path.length === size) {
        // 把当前路径拷贝一份放入结果集中。
        result.push([...path])
        // 当前分支已经完成，直接返回上一层。
        return
      }

      // 从 start 开始枚举当前还能选的处理器。
      for (let i = start; i < processors.length; i++) {
        // 先把当前处理器加入路径。
        path.push(processors[i])
        // 继续递归搜索下一个位置，避免重复选择，所以传 i + 1。
        dfs(i + 1)
        // 回溯，撤销当前选择，尝试下一个处理器。
        path.pop()
      }
    }

    // 从下标 0 开始 DFS，生成所有组合。
    dfs(0)
    // 返回最终组合结果。
    return result
  }

  // 如果任务申请 8 个处理器，必须要求 8 个处理器全部可用。
  if (num === 8) {
    // 若全部 8 个都可用，则只有一种组合；否则返回空数组。
    return available.length === 8 ? [available] : []
  }

  // 如果 num 不是题目允许的 1、2、4、8 之一，则直接返回空数组。
  if (![1, 2, 4].includes(num)) {
    // 返回空数组表示无合法组合。
    return []
  }

  // 建立不同申请数量对应的链路优先级规则映射。
  const priorityMap = {
    // num 为 1 时，优先选择链路可用数为 1、3、2、4 的情况。
    1: [1, 3, 2, 4],
    // num 为 2 时，优先选择链路可用数为 2、4、3 的情况。
    2: [2, 4, 3],
    // num 为 4 时，只能选择链路可用数为 4 的情况。
    4: [4]
  }

  // 把两条链路放进一个数组里，便于统一遍历。
  const links = [link1, link2]
  // 根据当前 num 取出对应的优先级列表。
  const priorities = priorityMap[num]

  // 按优先级顺序逐个尝试目标链路可用数量。
  for (let i = 0; i < priorities.length; i++) {
    // 当前这一轮正在尝试的目标链路可用数量。
    const targetSize = priorities[i]
    // 找出所有“可用处理器数量等于 targetSize”的链路。
    const matchedLinks = links.filter((link) => link.length === targetSize)
    console.log(matchedLinks, 'matchedLinks')

    // 如果这一优先级没有命中的链路，就继续看下一个优先级。
    if (matchedLinks.length === 0) {
      // 跳过当前优先级。
      continue
    }

    // 用 result 收集当前最佳优先级下所有链路的组合。
    const result = []
    // 遍历所有命中的链路。
    for (let j = 0; j < matchedLinks.length; j++) {
      // 把该链路上选 num 个处理器的所有组合追加到结果中。
      result.push(...buildCombinations(matchedLinks[j], num))
    }

    // 一旦找到当前最高优先级的结果，直接返回，不再看更低优先级。
    return result
  }

  // 如果所有优先级都没有找到合法链路，则返回空数组。
  return []
}

