// 定义主函数，n 表示员工总人数，records 表示 30 天的打卡记录。
const getTopClockInEmployees = (n, records) => {
  // 创建一个长度为 n 的数组，用来保存每个员工的统计信息。
  const stats = new Array(n).fill(0).map((_, id) => ({
    // 记录当前员工的 id。
    id,
    // 记录当前员工总共打卡了多少次。
    count: 0,
    // 记录当前员工第一次打卡的日期，初始设为无穷大表示还未打卡。
    firstDay: Infinity
  }))

  // 遍历每一天的打卡记录。
  for (let day = 0; day < records.length; day++) {
    // 取出当前这一天的打卡员工列表。
    const currentDayIds = records[day]

    // 遍历当天所有打卡员工。
    for (let i = 0; i < currentDayIds.length; i++) {
      // 取出当前员工 id。
      const employeeId = currentDayIds[i]
      // 让当前员工的打卡总次数加 1。
      stats[employeeId].count++
      // 如果这是该员工第一次打卡。
      if (stats[employeeId].firstDay === Infinity) {
        // 把首次打卡日期记录为当前 day。
        stats[employeeId].firstDay = day
      }
    }
  }

  // 过滤掉这个月一次都没有打过卡的员工。
  const validEmployees = stats.filter((item) => item.count > 0)

  // 按照题目要求对员工进行排序。
  validEmployees.sort((a, b) => {
    // 如果两个人打卡次数不同。
    if (b.count !== a.count) {
      // 打卡次数多的排前面。
      return b.count - a.count
    }

    // 如果两个人打卡次数相同，但首次打卡时间不同。
    if (a.firstDay !== b.firstDay) {
      // 更早开始打卡的排前面。
      return a.firstDay - b.firstDay
    }

    // 如果前两个条件都相同，则按 id 从小到大排序。
    return a.id - b.id
  })

  // 取出前 5 名员工，如果不足 5 名就全部返回。
  return validEmployees
    // 只截取前 5 个元素。
    .slice(0, 5)
    // 把员工对象映射成员工 id。
    .map((item) => item.id)
}

